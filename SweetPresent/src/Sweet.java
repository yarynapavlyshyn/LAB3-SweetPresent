public class Sweet {
    String name;
    int weight;
    int price;
    int sugar;
    int energy;

    Sweet () {}

    Sweet(String name, int weight, int price, int sugar, int energy) {
        this.name = name;
        this.weight = weight;
        this.price = price;
        this.sugar = sugar;
        this.energy = energy;
    }
}

